  /* ─── Idioma atual ─────────────────────────────────────────────── */
  let currentLang = localStorage.getItem('ha_lang') || 'pt';

  /* ─── Busca cotação em tempo real ─────────────────────────────── */
  function loadCachedRates() {
    try {
      const raw = localStorage.getItem('ha_rates');
      if (!raw) return false;
      const data = JSON.parse(raw);
      // Cache válido por 24h
      if (Date.now() - data.ts > 24 * 60 * 60 * 1000) return false;
      if (data.USD) rates.USD = data.USD;
      if (data.EUR) rates.EUR = data.EUR;
      return true;
    } catch (e) { return false; }
  }

  async function fetchRates() {
    try {
      const res = await fetch('https://open.er-api.com/v6/latest/BRL');
      if (!res.ok) return;
      const data = await res.json();
      if (data.rates) {
        rates.USD = data.rates.USD || rates.USD;
        rates.EUR = data.rates.EUR || rates.EUR;
        // Salva no cache
        try {
          localStorage.setItem('ha_rates', JSON.stringify({
            USD: rates.USD, EUR: rates.EUR, ts: Date.now()
          }));
        } catch (e) {}
        // Re-traduz preços com a nova cotação
        if (document.readyState !== 'loading') {
          document.querySelectorAll('[data-price-brl]').forEach(el => {
            const raw = el.dataset.priceBrl;
            if (raw !== undefined) el.textContent = formatPrice(raw, currentLang);
          });
          window.dispatchEvent(new CustomEvent('ha:ratesupdated'));
        }
      }
    } catch (e) {
      // usa fallback silenciosamente
    }
  }

  /* ─── Formata preço no idioma/moeda corretos ──────────────────── */
  /**
   * Parser robusto de valores monetários.
   * Aceita: "R$ 2.700.000", "2.700.000,50", "639361.74", "639,361.74"
   * Retorna número inteiro (centavos removidos — padrão alto luxo).
   */
  function parseMoney(text) {
    const cleaned = String(text)
      .replace(/r\$/gi, '')
      .replace(/\s/g, '')
      .replace(/[^\d.,]/g, '');
    if (!cleaned) return NaN;

    const lastComma = cleaned.lastIndexOf(',');
    const lastDot   = cleaned.lastIndexOf('.');

    let number;
    if (lastComma > -1 && lastDot > -1) {
      // Ambos presentes: o último é o separador decimal
      if (lastComma > lastDot) {
        // Formato BR: 1.234.567,89
        number = parseFloat(cleaned.replace(/\./g, '').replace(',', '.'));
      } else {
        // Formato US: 1,234,567.89
        number = parseFloat(cleaned.replace(/,/g, ''));
      }
    } else if (lastComma > -1) {
      // Apenas vírgula: decimal BR se 2 dígitos após, senão milhar
      const after = cleaned.length - lastComma - 1;
      number = after === 2
        ? parseFloat(cleaned.replace(/\./g, '').replace(',', '.'))
        : parseFloat(cleaned.replace(/,/g, ''));
    } else if (lastDot > -1) {
      // Apenas ponto: decimal US se 2 dígitos após, senão milhar BR
      const after = cleaned.length - lastDot - 1;
      number = after === 2
        ? parseFloat(cleaned)
        : parseFloat(cleaned.replace(/\./g, ''));
    } else {
      number = parseFloat(cleaned);
    }

    return number;
  }

  function formatPrice(brlString, lang) {
    const l = dict[lang] || dict.pt;
    const text = String(brlString || '').trim();

    // Sob consulta / Price on request
    if (!text || text.toLowerCase().includes('consulta') || text.toLowerCase().includes('request')) {
      return l.card_consult;
    }

    const number = parseMoney(text);
    if (!isFinite(number) || number <= 0) return text;

    // Imóveis de alto padrão: sem centavos
    const value = Math.round(number);

    if (lang === 'pt') {
      return new Intl.NumberFormat('pt-BR', {
        style: 'currency', currency: 'BRL',
        minimumFractionDigits: 0, maximumFractionDigits: 0,
      }).format(value);
    }

    if (lang === 'en') {
      const usd = Math.round(value * rates.USD);
      return new Intl.NumberFormat('en-US', {
        style: 'currency', currency: 'USD',
        minimumFractionDigits: 0, maximumFractionDigits: 0,
      }).format(usd).replace('$', 'US$ ');
    }

    if (lang === 'es') {
      const eur = Math.round(value * rates.EUR);
      return '€ ' + new Intl.NumberFormat('es-ES', {
        minimumFractionDigits: 0, maximumFractionDigits: 0,
      }).format(eur);
    }

    if (lang === 'fr') {
      const eur = Math.round(value * rates.EUR);
      return new Intl.NumberFormat('fr-FR', {
        minimumFractionDigits: 0, maximumFractionDigits: 0,
      }).format(eur) + ' €';
    }

    return text;
  }

  /* ─── Traduz todos os elementos com data-i18n ──────────────────── */
  function applyTranslations(lang) {
    const l = dict[lang] || dict.pt;

    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.dataset.i18n;
      if (!l[key]) return;
      if (el.dataset.i18nHtml !== undefined) {
        el.innerHTML = l[key];
      } else {
        el.textContent = l[key];
      }
    });

    // Traduz preços — inclui o preço do detalhe do imóvel
    document.querySelectorAll('[data-price-brl]').forEach(el => {
      const raw = el.dataset.priceBrl;
      if (raw !== undefined) el.textContent = formatPrice(raw, lang);
    });

    // Atualiza href do WhatsApp com mensagem contextual por página e idioma
    // Ignora links com data-wa-custom — esses são gerenciados pelo JS da página
    const page = (document.body.dataset.page || '').toLowerCase();
    let waKey = 'wa_msg_listing';
    if (page === 'bc') waKey = 'wa_msg_bc';
    else if (page === 'brava') waKey = 'wa_msg_brava';
    else if (page === 'home' || page === 'sobre' || page === 'contato') waKey = 'wa_msg_listing';
    const waMsg = encodeURIComponent(l[waKey] || l.wa_message || dict.pt.wa_message);
    document.querySelectorAll('a[data-wa]:not([data-wa-custom])').forEach(a => {
      const base = a.dataset.wa;
      a.href = `${base}?text=${waMsg}`;
    });

    // Atualiza currentLang ANTES de disparar o evento
    // para que getLang() já retorne o novo idioma quando os listeners reagirem
    currentLang = lang;
    localStorage.setItem('ha_lang', lang);

    // Atualiza lang do html
    document.documentElement.lang = lang === 'pt' ? 'pt-BR' : lang;

    // Marca botão ativo
    document.querySelectorAll('[data-lang-btn]').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.langBtn === lang);
    });

    // Notifica outros módulos — currentLang já está atualizado
    window.dispatchEvent(new CustomEvent('ha:langchange', { detail: { lang } }));
  }

  /* ─── Seletor de idioma (injeta no header) ─────────────────────── */
  function injectSelector() {
    const header = document.querySelector('.site-header');
    if (!header || document.querySelector('.lang-selector')) return;

    const selector = document.createElement('div');
    selector.className = 'lang-selector';
    selector.innerHTML = ['pt', 'en', 'es', 'fr'].map(lang => `
      <button
        class="lang-btn ${lang === currentLang ? 'active' : ''}"
        data-lang-btn="${lang}"
        onclick="HA_I18N.setLang('${lang}')"
      >
        ${lang.toUpperCase()}
      </button>
    `).join('');

    // Insere antes do botão CTA desktop
    const cta = header.querySelector('.desktop-cta');
    if (cta) {
      header.insertBefore(selector, cta);
    } else {
      header.appendChild(selector);
    }
  }

  /* ─── Re-aplica traduções em elementos dinâmicos (cards via JS) ── */
  function observeDynamic() {
    const targets = ['propertyGrid', 'bcCarousel', 'bravaCarousel', 'similarCarousel', 'mapList'];
    targets.forEach(id => {
      const el = document.getElementById(id);
      if (!el) return;
      let _obs_timer = null;
      new MutationObserver(() => {
        // Debounce — roda só 1x depois que o DOM estabilizar
        clearTimeout(_obs_timer);
        _obs_timer = setTimeout(() => {
          const l = dict[currentLang] || dict.pt;
          el.querySelectorAll('[data-i18n]').forEach(node => {
            const key = node.dataset.i18n;
            if (!l[key]) return;
            if (node.dataset.i18nHtml !== undefined) {
              node.innerHTML = l[key];
            } else {
              node.textContent = l[key];
            }
          });
        }, 50);
      // childList only — não observa subtree para não disparar por cada filho
      }).observe(el, { childList: true });
    });
  }

  /* ─── Init ─────────────────────────────────────────────────────── */
  function init() {
    // Carrega cotação do cache imediatamente (sem rede)
    loadCachedRates();
    // Aplica traduções na hora — não espera a rede
    injectSelector();
    applyTranslations(currentLang);
    observeDynamic();
    // Busca cotação atualizada em segundo plano (não bloqueia render)
    fetchRates();
  }

  /* ─── API pública ──────────────────────────────────────────────── */
  function setLang(lang) {
    if (!dict[lang]) return;
    applyTranslations(lang);
  }

  function t(key) {
    const l = dict[currentLang] || dict.pt;
    return l[key] || dict.pt[key] || key;
  }

  function getLang() { return currentLang; }
  function price(brl) { return formatPrice(brl, currentLang); }

  document.addEventListener('DOMContentLoaded', init);

  return { setLang, t, getLang, price, formatPrice, _rates: rates };

